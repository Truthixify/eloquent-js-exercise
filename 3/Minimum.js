// Define a 'min' function using arrow notation to find the minimum of two numbers.
const min = (a, b) => a > b ? b : a;

// Output the results of the 'min' function with different arguments.
console.log(min(0, -2));     // Output: -2 (minimum of 0 and -2)
console.log(min(5, -9));     // Output: -9 (minimum of 5 and -9)
console.log(min(100, 299));  // Output: 100 (minimum of 100 and 299)
